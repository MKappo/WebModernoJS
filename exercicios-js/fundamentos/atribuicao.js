const a = 7 // = não compara, mas sim atribui
let b = 3

b += a //Pegando valor de B e somando o valor de A e então atribuindo a B (atribuição aditiva b = b+ a)
console.log(b)

b = a //Que é diferente neste caso, pois estou ignorando valor de b para atribuir a ele o valor de A
console.log(b)

b -= 4 //Mesma ideia porém uma atribuição subtrativa. b = b - 4
console.log(b)

b *= 4 //Atribuição multiplicativa.  b = b * 4
console.log(b)

b /= 2 //Atribuição divisiva. b =  b / 2
console.log(b)

b %= 2 //Atribuição modular. b = b % 2  (modular é o resto da divisão)
console.log(b)





